#Exe3 - Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre sua média.
n1 = int(input('Digite a primeira nota: '))
n2 = int(input('Digite a segunda nota: '))
n3 = int(input('Digite a terceira nota: '))
print('A soma das duas notas é: {} \n e sua média é: {:1}'.format(n1+n2+n3, (n1+n2+n3)/3))
